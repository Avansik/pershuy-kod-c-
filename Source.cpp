#include <iostream>

using namespace std;

template<typename T>
class Queue 
{
public:
    struct Element 
    {
        T value;
        int priority;
        void Print()
        {
            cout << "Value : " << value << ". Priority : " << priority << endl;
        }
    };

private:
    Element* arr;
    int size;

public:
    Queue() : arr(nullptr), size(0) {}
    ~Queue() 
    {
        if (arr != nullptr) delete[] arr;
    }

    void Enqueue(T value, int pr) 
    {
        int index = 0;
        while (index < size && arr[index].priority < pr) 
        {
            index++;
        }
        size++;
        Element* temp = new Element[size];
        for (int i = 0; i < index; i++) 
        {
            temp[i] = arr[i];
        }
        temp[index] = Element{ value, pr };
        for (int i = index; i < size - 1; i++)
        {
            temp[i + 1] = arr[i];
        }
        if (arr != nullptr) 
        {
            delete[] arr;
        }
        arr = temp;
    }

    bool IsEmpty() const
    {
        return size == 0;
    }

    Element Dequeue() 
    {
        if (!IsEmpty()) 
        {
            Element first = arr[0];
            for (int i = 0; i < size - 1; i++) 
            {
                arr[i] = arr[i + 1];
            }
            size--;
            return first;
        }
         
    }

    void Print() const
    {
        cout << "--------------- Queue with priority ----------------" << endl;
        for (int i = 0; i < size; i++) {
            cout << arr[i].value << " : " << arr[i].priority << " | ";
        }
        cout << endl;
        cout << "____________________________________________________" << endl;
    }
};

